
import UIKit


extension UIFont {
    class func SFProText_UltraLight(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "SFProText-Ultralight", size: size)!
    }
    
    class func SFProText(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "SFProText-Regular", size: size)!
    }
    class func SFProText_Bold(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "SFProText-Bold", size: size)!
    }
    class func SFProText_Light(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "SFProText-Light", size: size)!
    }
    class func SFProText_Medium(size: CGFloat = UIFont.systemFontSize) -> UIFont {
        return UIFont(name: "SFProText-Medium", size: size)!
    }

}
